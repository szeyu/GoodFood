package com.example.goodfood;

public class Nutrient {
    // Temporary nutrient class for data
    public String name;
    public float amountTaken;
    public float suggestedAmount;

    public Nutrient(String name, float amountTaken, float suggestedAmount) {
        this.name = name;
        this.amountTaken = amountTaken;
        this.suggestedAmount = suggestedAmount;
    }
}
